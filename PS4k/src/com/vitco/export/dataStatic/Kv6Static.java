    private static final double GOLDRAT = 0.3819660112501052;

    private static double[] equiind2vec(long i, double zmulk, double zaddk) {
        double[] result = new double[3];

        result[2] = i*zmulk + zaddk;
        double r = Math.sqrt(1.f - result[2]*result[2]);
        double val = i * (GOLDRAT * Math.PI * 2);
        result[0] = Math.cos(val) * r;
        result[1] = Math.sin(val) * r;

        return result;
    }

    private static TIntObjectHashMap<double[]> equivecp = new TIntObjectHashMap<double[]>();

    // generate buffer
    private static void equimemset (int n) {
        double zmulk = 2 / (float)n;
        double zaddk = zmulk * .5 - 1.0;
        for (int z = n-1; z >= 0; z--) {
            double[] result = equiind2vec(z, zmulk, zaddk);
            equivecp.put(z, result);
        }
        equivecp.put(n, new double[3]);
    }

    // print distrinct directions
    private static void generateDistinctDirections() {
        int directions = 255;
        equimemset(directions);

        // print result
        for (int i = 0; i <= directions; i++) {
            double[] entry = equivecp.get(i);
            System.out.println("new double[] {" + entry[0] + ", " + entry[1] + ", " + entry[2] + "},");
            System.out.println(Kv6Static.directions[i][0] + ", " + Kv6Static.directions[i][1] + ", " + Kv6Static.directions[i][2]);
        }
    }

    // =============

    // print circle values
    private static void generateCircleValues() {
        int radius = 3;

        // loop over different radii
        for (int xr = -radius; xr <= radius; xr++) {
            for (int yr = -radius; yr <= radius; yr++) {
                for (int zr = -radius; zr <= radius; zr++) {
                    // check that point is in sphere
                    if (xr * xr + yr * yr + zr * zr <= (radius + 1) * (radius + 1)) {
                        // first entry is the offset that can be used to efficiently shift the
                        // posId bit of the CubeIndexer (without the need to shift three distinct values)
                        System.out.println("new int[] {" + CubeIndexer.getShiftOperand(xr, yr, zr) + ", " + xr + ", " + yr + ", " + zr + "},");
                    }
                }
            }
        }
    }

    // offline computations
    public static void main(String[] args) {
        generateCircleValues();
        generateDistinctDirections();
    }

